from strands import Agent, tool


@tool
def hr_questions(query: str) -> str:
    """
    人事がよく使う「応募者への質問の内容」を返すツール。
    """
    return (
        "ありがとうございます。まずは最初の質問をさせていただきます。\n\n"
        "「あなたの生成Ai開発経験と、それをどのように業務で活かしてきたか教えてください。」\n\n"
        "これまでのプログラミング経験や、具体的にどのようなプロジェクトで生成AIを使用されたか、"
        "また業務上でどのような成果を上げることができたかについて、詳しくお聞かせください。\n\n"
        "今までの業務で一番苦労したことをお聞かせください。\n\n"
        "これまでの業務実績を重点確認\n\n"
        "求めている人材像を明確に表現する。求職者に期待することについては、はっきりと伝える。\n\n"
        "入社時の動機と退職動機にギャップや目的転換の理由などがないか確認する\n\n"
        "自分自身をどのような人物であると考えていますか？\n\n"
        "上司や同僚の方から見て、あなたはどのような人ですか？\n\n"
        "入社時と現在とでは、自分自身がどのように成長していると思いますか？\n\n"
        "弊社があなたの友人に対して、あなたの採用合否についてアドバイスを求めたら、どのように答えると思いますか？\n\n"
        "あなたの最大の強みはなんですか？\n\n"
        "あなたの強みを全て上げてください。\n\n"
        "あなたの一番の弱点・欠点は何ですか\n\n"
        "現職において、あなたの最大の成果（失敗）は何ですか？\n\n"
        "弊社があなたを雇う決め手はどのような点にあるとお考えですか？\n\n"
        "業務上、上司と意見が合わなかった経験はありますか？\n\n"
        "これまでの最高の上司と最低の上司はどのような方でしたか？\n\n"
        "上司には、どのような業務関与をして欲しいですか？\n\n"
        "チームで業務に取り組んだ際のエピソードを押してください。\n\n"
        "入社後、どのくらいの期間でフィットできると思いますか？\n\n"
        "個人とチーム、どちらで業務を進めるほうが得意ですか？\n\n"
        "現在の職場で抱えている課題を教えてください。\n\n"
        "これまでに直面した難題について教えてください。また、どのように解決しましたか？\n\n"
        "業務負荷の高い仕事や責任の重責を果たしたとき、どのような事を心がけましたか？\n\n"
        "業務中に「怒り」を感じた経験はありますか？それはどのようなシーンで起こったことですか？\n\n"
        "上司から受けた印象的なフィードバックはどのような内容でしたか？\n\n"
        "なぜ弊社で働きたいと思ったのか教えてください。\n\n"
        "どのような視点で志望企業を選んでいますか？\n\n"
        "具体的に弊社のどういった点に興味がありますか？\n\n"
        "入社後、30日後、60日後、90日後にどのような働き方をイメージしているのか教えてください。\n\n"
        "5年後の目標を教えてください。\n\n"
        "転職先を見つけるためにどんな取り組みをしていますか？\n\n"
        "弊社ではどのような役割が求められていると思いますか？\n\n"
        "実績をもとに、どのような貢献ができると考えていますか？\n\n"
        "弊社はどのような企業だとお考えですか？印象を教えてください。\n\n"
        "今回の求人募集に、あなたが適任である理由を教えてください。\n\n"
        "配属予定の上司や同僚について質問はありますか？\n\n"
        "どのような職場環境で働きたいですか？\n\n"
        "業務上、最も報われた（報われなかった）経験を教えてください。\n\n"
        "印象に残っている、特に力を発揮した、業務はどのようなことでしたか？\n\n"
        "成功（理想の仕事）とはどのようなものですか？\n\n"
        "新しい職場や仕事で達成したいことはありますか？\n\n"
        "キャリア（人生）での目標を教えてください。\n\n"
        "これまでとキャリアプランを変更する理由を教えてください。\n\n"
    )


hr_agent = Agent(
    name="HRAgent",
    description="人事担当。応募者の人物像や志望動機・成果を深掘りする。",
    system_prompt=(
        "あなたは企業の人事担当者です。応募者との面接を行います。\n"
        "開発部門の責任者（Dept）と一緒に面接を行っており、交互またはランダムに質問します。\n\n"
        "・あなたの役割は、応募者の人物像・志望動機・これまでの実績を理解することです。\n"
        "・直前までの会話ログ（HR, Dept, Applicant の発話）を読んで、必要に応じて質問またはコメントを1つだけ返してください。\n"
        "・面接全体を終了してよいと判断した場合は、あなたの最後のメッセージの末尾に必ず `<INTERVIEW_DONE>` を付けてください。\n\n"
        "出力は常に日本語で、自然な面接としてふるまってください。"
    ),
    tools=[hr_questions],
)
